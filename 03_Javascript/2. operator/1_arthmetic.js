/**
 * 사칙 연산 : +, -, *, /, %, **
 */
console.log('========================================');
console.log('');

console.log(3 + 5);
console.log(3 - 5);
console.log(3 * 5);
console.log(3 / 5);
console.log(3 % 5);
console.log(3 ** 5);

console.log('');
console.log('========================================');
console.log('');

// 짝수, 홀수 선택 : %
let a = 101;
console.log(a % 2);
console.log((a % 2) ? "홀수" : "짝수");

let a2 = 100;
console.log(a2 % 2);
console.log((a2 % 2) ? "홀수" : "짝수");

console.log('');
console.log('========================================');
console.log('');

// 접합연산자 : +
console.log(10 + 20);
console.log('10' + 20);
console.log('합계 = ' + a);
console.log(`합계 = ${a}`);